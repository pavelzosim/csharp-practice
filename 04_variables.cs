using System;

namespace CSLearn
{
    internal class Program
    {
        static void Main(string[] args)
        {
            // ============================================
            // ЦЕЛОЧИСЛЕННЫЕ ТИПЫ (Integer Types)
            // ============================================

            // byte - беззнаковый 8-битный тип
            // Размер: 1 байт | Диапазон: 0 до 255
            // Применение: хранение маленьких положительных чисел (возраст, проценты, RGB цвета)
            // Пример: byte health = 100; // HP игрока от 0 до 255
            byte b = 255;

            // sbyte - знаковый 8-битный тип
            // Размер: 1 байт | Диапазон: -128 до 127
            // Применение: маленькие числа с отрицательными значениями (температура, смещения)
            // Пример: sbyte temperature = -40; // температура в Цельсиях
            sbyte sb = -128;

            // short - знаковый 16-битный тип
            // Размер: 2 байта | Диапазон: -32,768 до 32,767
            // Применение: координаты в небольших картах, счетчики, инвентарь
            // Пример: short posX = 1024; // координата X на карте
            short s = -32768;

            // ushort - беззнаковый 16-битный тип
            // Размер: 2 байта | Диапазон: 0 до 65,535
            // Применение: порты сетевых соединений, ID объектов в небольших системах
            // Пример: ushort port = 8080; // номер порта сервера
            ushort us = 65535;

            // int - знаковый 32-битный тип (САМЫЙ ИСПОЛЬЗУЕМЫЙ!)
            // Размер: 4 байта | Диапазон: -2,147,483,648 до 2,147,483,647
            // Применение: большинство целочисленных вычислений, счетчики, индексы, ID
            // Пример: int score = 1000000; // счет в игре
            // TIP: используйте int по умолчанию для целых чисел, если нет особых требований к памяти
            int i = -2147483648;

            // uint - беззнаковый 32-битный тип
            // Размер: 4 байта | Диапазон: 0 до 4,294,967,295
            // Применение: битовые маски, цвета RGBA, большие положительные числа
            // Пример: uint color = 0xFF5733FF; // цвет в формате RGBA
            uint ui = 4294967295;

            // long - знаковый 64-битный тип
            // Размер: 8 байт | Диапазон: -9,223,372,036,854,775,808 до 9,223,372,036,854,775,807
            // Применение: временные метки (timestamp), большие вычисления, ID в огромных базах данных
            // Пример: long timestamp = 1699999999999; // Unix timestamp в миллисекундах
            long l = -9223372036854775808;

            // ulong - беззнаковый 64-битный тип
            // Размер: 8 байт | Диапазон: 0 до 18,446,744,073,709,551,615
            // Применение: очень большие положительные числа, размеры файлов
            // Пример: ulong fileSize = 5368709120; // размер файла в байтах (5 GB)
            ulong ul = 18446744073709551615;

            // ============================================
            // ЧИСЛА С ПЛАВАЮЩЕЙ ТОЧКОЙ (Floating Point)
            // ============================================

            // float - 32-битное число с плавающей точкой
            // Размер: 4 байта | Точность: ~6-7 значащих цифр
            // Применение: 3D координаты в играх, физические расчеты (где не нужна высокая точность)
            // Пример: float velocity = 9.8f; // скорость объекта
            // TIP: всегда добавляйте суффикс 'f'! Без него компилятор считает число double
            // TIP: float быстрее double на некоторых платформах и занимает меньше памяти
            float f = 5.7f;

            // double - 64-битное число с плавающей точкой (ПО УМОЛЧАНИЮ для дробных)
            // Размер: 8 байт | Точность: ~15-16 значащих цифр
            // Применение: научные расчеты, математика, когда нужна точность
            // Пример: double pi = 3.14159265358979; // число Пи
            // TIP: используйте double, когда точность важнее памяти и скорости
            double d = 5.7;

            // decimal - 128-битное десятичное число высокой точности
            // Размер: 16 байт | Точность: ~28-29 значащих цифр
            // Применение: финансовые расчеты, деньги, точные математические вычисления
            // Пример: decimal price = 19.99m; // цена товара
            // TIP: обязателен суффикс 'm'! Decimal избегает ошибок округления в денежных расчетах
            // WARNING: decimal медленнее float/double, но точнее для десятичных дробей
            decimal dec = 5.7m;

            // ============================================
            // ДРУГИЕ ТИПЫ
            // ============================================

            // char - Unicode символ
            // Размер: 2 байта | Диапазон: '\u0000' до '\uffff'
            // Применение: отдельные символы, клавиши, буквы
            // Пример: char grade = 'A'; // оценка студента
            // TIP: используйте одинарные кавычки! Двойные - это string
            char c = 'A';

            // string - последовательность символов (ссылочный тип!)
            // Размер: динамический | Неизменяемый (immutable)
            // Применение: текст, имена, сообщения, любые строковые данные
            // Пример: string playerName = "Steve"; // имя игрока
            // TIP: string неизменяем! При изменении создается новый объект в памяти
            // TIP: для частых изменений строк используйте StringBuilder
            string str = "Hello, World!";

            // bool - логический тип
            // Размер: 1 байт | Значения: true или false
            // Применение: флаги, условия, состояния (включено/выключено)
            // Пример: bool isAlive = true; // жив ли персонаж
            bool bo = true;

            // ============================================
            // МАТЕМАТИЧЕСКИЕ ОПЕРАЦИИ
            // ============================================

            int a = 10;
            int b2 = 3;

            // --- БАЗОВЫЕ АРИФМЕТИЧЕСКИЕ ОПЕРАЦИИ ---

            int sum = a + b2;           // Сложение: 10 + 3 = 13
            int diff = a - b2;          // Вычитание: 10 - 3 = 7
            int mult = a * b2;          // Умножение: 10 * 3 = 30

            // ДЕЛЕНИЕ - важные моменты!
            int divInt = a / b2;        // Целочисленное деление: 10 / 3 = 3 (дробная часть отбрасывается!)
            double divDouble = (double)a / b2;  // Деление с дробной частью: 10.0 / 3 = 3.333...
                                                // TIP: приведение хотя бы одного операнда к double/float даст дробный результат
                                                // WARNING: деление - самая медленная арифметическая операция (~10-40 тактов CPU)
                                                // PERFORMANCE: если делите на степень 2, используйте битовый сдвиг: x / 4 → x >> 2
                                                // PERFORMANCE: если возможно, замените деление на умножение: x / 5.0 → x * 0.2

            int remainder = a % b2;     // Остаток от деления (modulo): 10 % 3 = 1
                                        // Применение: проверка четности (x % 2 == 0), циклические операции, хеширование

            // --- ИНКРЕМЕНТ И ДЕКРЕМЕНТ ---

            int x = 5;
            int postInc = x++;          // Постфиксный: сначала возвращает 5, потом x становится 6
            int preInc = ++x;           // Префиксный: сначала x становится 7, потом возвращает 7

            int y = 5;
            int postDec = y--;          // Постфиксный: возвращает 5, потом y = 4
            int preDec = --y;           // Префиксный: y = 3, возвращает 3

            // TIP: в циклах for (int i = 0; i < 10; i++) оба варианта работают одинаково
            // TIP: используйте префиксный (++i) когда результат не используется - чуть быстрее

            // --- СОСТАВНЫЕ ОПЕРАТОРЫ ПРИСВАИВАНИЯ ---

            int z = 10;
            z += 5;     // z = z + 5;  → z = 15
            z -= 3;     // z = z - 3;  → z = 12
            z *= 2;     // z = z * 2;  → z = 24
            z /= 4;     // z = z / 4;  → z = 6
            z %= 5;     // z = z % 5;  → z = 1

            // TIP: составные операторы короче и иногда быстрее обычных
            // Применение: счетчики, накопление значений, обновление состояний

            // --- МАТЕМАТИЧЕСКИЕ ФУНКЦИИ (Math класс) ---

            int abs = Math.Abs(-5);             // Модуль числа: |-5| = 5
            double pow = Math.Pow(2, 3);        // Возведение в степень: 2³ = 8
            double sqrt = Math.Sqrt(16);        // Квадратный корень: √16 = 4
                                                // WARNING: Math.Pow медленная! Для маленьких степеней лучше x*x*x

            int max = Math.Max(5, 10);          // Максимум из двух чисел: 10
            int min = Math.Min(5, 10);          // Минимум из двух чисел: 5

            double round = Math.Round(5.7);     // Округление до ближайшего целого: 6
            double floor = Math.Floor(5.7);     // Округление вниз: 5
            double ceiling = Math.Ceiling(5.3); // Округление вверх: 6

            // Тригонометрия (углы в радианах!)
            double sin = Math.Sin(Math.PI / 2); // sin(90°) = 1
            double cos = Math.Cos(0);           // cos(0°) = 1
                                                // TIP: для преобразования градусов в радианы: radians = degrees * Math.PI / 180

            // --- ОПЕРАЦИИ СРАВНЕНИЯ ---
            // Возвращают bool (true/false)

            bool isEqual = (5 == 5);        // Равно: true
            bool notEqual = (5 != 3);       // Не равно: true
            bool greater = (5 > 3);         // Больше: true
            bool less = (5 < 3);            // Меньше: false
            bool greaterOrEq = (5 >= 5);    // Больше или равно: true
            bool lessOrEq = (5 <= 3);       // Меньше или равно: false

            // TIP: для сравнения float/double используйте Math.Abs(a - b) < epsilon
            // Причина: ошибки округления (0.1 + 0.2 != 0.3 в компьютерах!)

            // --- ЛОГИЧЕСКИЕ ОПЕРАЦИИ ---

            bool andOp = true && false;     // Логическое И (AND): false (оба должны быть true)
            bool orOp = true || false;      // Логическое ИЛИ (OR): true (хотя бы один true)
            bool notOp = !true;             // Логическое НЕ (NOT): false (инверсия)

            // TIP: && и || используют "короткое замыкание" (short-circuit evaluation)
            // Пример: if (x != 0 && 10/x > 2) - второе условие не проверяется если x == 0
            // Применение: безопасные проверки, оптимизация (ставьте быстрые проверки первыми)

            // --- БИТОВЫЕ ОПЕРАЦИИ (для продвинутых) ---

            int bitAnd = 5 & 3;         // Побитовое И: 0101 & 0011 = 0001 (1)
            int bitOr = 5 | 3;          // Побитовое ИЛИ: 0101 | 0011 = 0111 (7)
            int bitXor = 5 ^ 3;         // Побитовое исключающее ИЛИ: 0101 ^ 0011 = 0110 (6)
            int bitNot = ~5;            // Побитовое НЕ: ~0101 = 1010 (-6 в дополнительном коде)
            int leftShift = 5 << 1;     // Сдвиг влево: 0101 << 1 = 1010 (10) | Умножение на 2
            int rightShift = 5 >> 1;    // Сдвиг вправо: 0101 >> 1 = 0010 (2) | Деление на 2

            // Применение: флаги, маски, оптимизация (быстрее * и /), работа с цветом
            // Пример флагов: const int READ = 1; const int WRITE = 2; int permissions = READ | WRITE;
            // PERFORMANCE: битовые операции очень быстрые (~1 такт CPU)
        }
    }
}